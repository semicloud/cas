package org.semicloud.cas.shared.intensity.oval2;

/**
 * 线源参数
 * 
 * @author Semicloud
 *
 */
public class LineParams {
	private double lia;
	private double lib;
	private double lic;

	private double ra;
	private double rb;
	private double rc;
	private double rr;

	private double base;

	/**
	 * @return the lia
	 */
	public double getLia() {
		return lia;
	}

	/**
	 * @param lia
	 *            the lia to set
	 */
	public void setLia(double lia) {
		this.lia = lia;
	}

	/**
	 * @return the lib
	 */
	public double getLib() {
		return lib;
	}

	/**
	 * @param lib
	 *            the lib to set
	 */
	public void setLib(double lib) {
		this.lib = lib;
	}

	/**
	 * @return the lic
	 */
	public double getLic() {
		return lic;
	}

	/**
	 * @param lic
	 *            the lic to set
	 */
	public void setLic(double lic) {
		this.lic = lic;
	}

	/**
	 * @return the ra
	 */
	public double getRa() {
		return ra;
	}

	/**
	 * @param ra
	 *            the ra to set
	 */
	public void setRa(double ra) {
		this.ra = ra;
	}

	/**
	 * @return the rb
	 */
	public double getRb() {
		return rb;
	}

	/**
	 * @param rb
	 *            the rb to set
	 */
	public void setRb(double rb) {
		this.rb = rb;
	}

	/**
	 * @return the rc
	 */
	public double getRc() {
		return rc;
	}

	/**
	 * @param rc
	 *            the rc to set
	 */
	public void setRc(double rc) {
		this.rc = rc;
	}

	/**
	 * @return the rr
	 */
	public double getRr() {
		return rr;
	}

	/**
	 * @param rr
	 *            the rr to set
	 */
	public void setRr(double rr) {
		this.rr = rr;
	}

	/**
	 * @return the base
	 */
	public double getBase() {
		return base;
	}

	/**
	 * @param base
	 *            the base to set
	 */
	public void setBase(double base) {
		this.base = base;
	}
}
